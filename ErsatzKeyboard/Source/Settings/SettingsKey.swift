public enum SettingsKey {
    public static let autoCapitalization = "kAutoCapitalization"
    
    /// Setting for a shortcut to insert a period after two consecutive spaces
    public static let periodShortcut = "kPeriodShortcut"
    
    /// Setting to play keyboard click sound effects
    public static let keyboardClicks = "kKeyboardClicks"
    
    /// Setting to display lowercase letters on keycaps if shift is not on
    public static let smallLowercase = "kSmallLowercase"
}
